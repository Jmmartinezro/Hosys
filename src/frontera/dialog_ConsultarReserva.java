    /**
     * Al seleccionarse el campo de texto "textField_BuscarPor" se procederá a cambiar el estilo de
     * y el color del texto y se borrará el texto por defecto "Código de reserva" ó
     * "Documento de identidad", dependiendo cual sea el caso.
     * @param evt selección del campo de texto "textField_BuscarPor"
     */
    private void textField_BuscarPorFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_textField_BuscarPorFocusGained
        if (textField_BuscarPor.getText().equals("Código de reserva") || textField_BuscarPor.getText().equals("Documento de identidad")) {
            textField_BuscarPor.setFont(new Font(textField_BuscarPor.getFont().getName(), Font.PLAIN, 15));
            textField_BuscarPor.setForeground(Color.BLACK);
            textField_BuscarPor.setText("");
        }
    }//GEN-LAST:event_textField_BuscarPorFocusGained
    /**
     * Al seleccionarse otro elemento del panel, se cambiará el estilo y color del
     * texto y se pondrá el texto por defecto "Código de reserva" ó
     * "Documento de identidad", dependiendo cual sea el caso.
     * @param evt de-selección del campo de texto "textField_BuscarPor".
     */
    private void textField_BuscarPorFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_textField_BuscarPorFocusLost
        if (textField_BuscarPor.getText().equals("")) {
            textField_BuscarPor.setFont(new Font(textField_BuscarPor.getFont().getName(), Font.ITALIC, 15));
            textField_BuscarPor.setForeground(Color.GRAY);
            if (radioButton_CodigoDeReserva.isSelected()) {
                textField_BuscarPor.setText("Código de reserva");
            } else if (radioButton_DNI.isSelected()) {
                textField_BuscarPor.setText("Documento de identidad");
            }
        }
    }//GEN-LAST:event_textField_BuscarPorFocusLost
    /**
     * Permite la validación de datos en el campo de texto "textField_BuscarPor"
     * cada vez que libere una tecla. Si algún caracter es erróneo, se bloqueará el
     * boton "button_Buscar" hasta que se haya corregido el error.
     * @param evt liberación de tecla en el campo de texto "textField_BuscarPor"
     */
    private void textField_BuscarPorKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_textField_BuscarPorKeyReleased
        ValidarEntrada validador = new ValidarEntrada();
        presionarEsc(evt, this);
        if (label_BuscarPor.getText().equals("Código de reserva")) {
            if (validador.ValidarNumeros(textField_BuscarPor.getText(), 3) || (textField_BuscarPor.getText().isEmpty())) {
                button_Buscar.setEnabled(true);
                textField_BuscarPor.setForeground(Color.black);
            } else {
                textField_BuscarPor.setForeground(Color.red);
                button_Buscar.setEnabled(false);
            }
        } else if (label_BuscarPor.getText().equals("Documento de identidad")) {
            if (validador.ValidarNumeros(textField_BuscarPor.getText(), 1) || (textField_BuscarPor.getText().isEmpty())) {
                button_Buscar.setEnabled(true);
                textField_BuscarPor.setForeground(Color.black);
            } else {
                textField_BuscarPor.setForeground(Color.red);
                button_Buscar.setEnabled(false);
            }
        }
        if (textField_BuscarPor.getText().equals("")) {
            button_Buscar.setEnabled(false);
        }
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (button_Buscar.isEnabled()) {
                if (textField_BuscarPor.getText().equals("Código de reserva")) {
                    ArrayList<Reserva> reservas = controlHab.reservasExistentes();
                    if (reservas.isEmpty()) {
                        JOptionPane.showMessageDialog(this, "No hay Reservas", "Error", JOptionPane.ERROR_MESSAGE, new javax.swing.ImageIcon(getClass().getResource("/frontera/imagenes/iconos/m/remove.png")));
                    } else {
                        parent.cambiarPanel(new panel_ReservasExistentes(parent));
                        dispose();
                    }

                } else {
                    Reserva reserva = new AdministrarReserva().ConsultarReserva(Long.parseLong(textField_BuscarPor.getText()), radioButton_CodigoDeReserva.isSelected());
                    if (reserva != null) {
                        parent.cambiarPanel(new panel_AdministrarReserva(parent, 3, reserva));
                        dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Error, Reserva no encontrada!", "Error", JOptionPane.ERROR_MESSAGE, new javax.swing.ImageIcon(getClass().getResource("/frontera/imagenes/iconos/m/remove.png")));
                    }
                }
            }
        }
    }//GEN-LAST:event_textField_BuscarPorKeyReleased

