                JOptionPane.showMessageDialog(this, "Error, Reserva no encontrada!", "Error", JOptionPane.ERROR_MESSAGE, new javax.swing.ImageIcon(getClass().getResource("/frontera/imagenes/iconos/m/remove.png")));
